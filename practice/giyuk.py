import turtle
wn=turtle.Screen()
t1=turtle.Turtle()
turnby=45
oldpos=t1.pos()
oldhead=t1.heading()
def giyuk() : 
    t1.fd(100)
    t1.rt(90)
    t1.fd(100)
giyuk()
t1.penup()
t1.setpos(oldpos)
t1.setheading(oldhead+turnby)
t1.pendown()
giyuk()
t1.penup()
t1.setpos(oldpos)
t1.setheading(90)
t1.pendown()
giyuk()
t1.penup()
t1.setpos(oldpos)
t1.setheading(135)
t1.pendown()
giyuk()
t1.penup()
t1.setpos(oldpos)
t1.setheading(180)
t1.pendown()
giyuk()
t1.penup()
t1.setpos(oldpos)
t1.setheading(225)
t1.pendown()
giyuk()
t1.penup()
t1.setpos(oldpos)
t1.setheading(270)
t1.pendown()
giyuk()
t1.penup()
t1.setpos(oldpos)
t1.setheading(315)
t1.pendown()
giyuk()
wn.exitonclick()
